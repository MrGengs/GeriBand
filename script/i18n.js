  /* ── Core translate function ─────────────────────────── */
  function t(key, vars = {}) {
    const str = strings[_lang]?.[key] ?? strings['id']?.[key] ?? key;
    return str.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? '');
  }

  /* ── Language switching ──────────────────────────────── */
  function setLang(lang) {
    if (!strings[lang]) return;
    _lang = lang;
    localStorage.setItem(LANG_KEY, lang);
    _notifyChange();
  }

  function getLang()   { return _lang; }
  function isEn()      { return _lang === 'en'; }
  function isId()      { return _lang === 'id'; }

  /* ── Change listeners ────────────────────────────────── */
  function onChange(fn) { _listeners.push(fn); }
  function _notifyChange() { _listeners.forEach(fn => fn(_lang)); }

  return { t, setLang, getLang, isEn, isId, onChange };
